const diaDaSemana = 7

if(diaDaSemana === 1) {
    console.log('Domingo')
}else if(diaDaSemana === 2) {
    console.log('Segunda')
}else if(diaDaSemana === 3){ 
    console.log('Terça')
}else if(diaDaSemana === 4) {
    console.log('Quarta')
}else if(diaDaSemana === 5){ 
    console.log('Quinta')
}else if(diaDaSemana === 6) {
    console.log('Sexta')
}else if(diaDaSemana === 7){ 
    console.log('Sabado')
}else{
    console.log('Opção Inválida')
}